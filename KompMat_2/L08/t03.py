# Задано послідовність дійсних чисел a_1,a_2,...,a_n.
# Визначити суму та кількість додатних елементів,
# індекси яких діляться на 3 без остачі.
N = int(input())
lst = [float(el) for el in input().split()]
# [6.0, 7.5, 2.1, 2.0, 0.0, -3.0]
#    0    1    2    3    4     5   індекси масиву
#    1    2    3    4    5     6   індекси послідовності
count = 0
suma = 0
for i in range(2, N, 3):
    el = lst[i]
    if el > 0:
        count += 1
        suma += el

print(count, suma)

# Задано рядок, що складається з
# послідовності символів латинського алфавіту та символу пропуску.
# Словом вважатимемо послідовність символів лат.алф, що не містить смволу пропуску
s = "Hello    world   Hello   world   hello   World"
# Підрахувати кількість різних слів, що входять до рядка
s = s.lower()
words = s.split()
uniq_words = set(words)
print("кількість різних слів у рядку = ", len(uniq_words))

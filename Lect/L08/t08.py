# Ввести з клавіатури у список,
# послідовність натуральних елементів кожен
# з яких вводиться з нового рядка,
# а послідовність завершується нулем

lst = []  # порожній список у який будемо додавати зчитані з клавіатури числа
while True:
    a = int(input("a[..] = "))
    if a == 0:
        break
    lst.append(a)

print(lst)


# 3 5: 5-3 = 2, 3-2=1, 2-1=1, 1-1=0

# N  M    D: N % M
# 12 15:
# 15 12              # відсортували
# 15 % 12 = 3,
# 12 % 3  = 0
N = int(input("Введіть перше число "))
M = int(input("Введіть друге число "))
n = N
m = M
if n < m:
    p = n
    n = m
    m = p

while m > 0:
    D = n % m
    n = m
    m = D

print(f"Найбільший спільний дільник чисел {N} та {M} = {n}")


